export function getArticleById(id) {
  return ARTICLES.find((article) => article.id === id) || null;
}

export function getArticleBlocks(article, lang) {
  return lang === 'th' && article.bodyTh ? article.bodyTh : article.body;
}

export function getArticleMeta(article, lang, baseUrl) {
  if (lang === 'th') {
    return {
      title: `${article.titleTh} | เบลสมี ไทยแลนด์`,
      description: article.excerptTh,
      canonical: `${baseUrl}/blog/${article.id}`,
    };
  }

  return {
    title: `${article.title} | BlessMe Thailand`,
    description: article.excerpt,
    canonical: `${baseUrl}/blog/${article.id}`,
  };
}

export function getArticleSchema(article, lang) {
  const schema = {
    '@context': 'https://schema.org',
    '@type': 'BlogPosting',
    headline: lang === 'th' ? article.titleTh : article.title,
    description: lang === 'th' ? article.excerptTh : article.excerpt,
    inLanguage: lang === 'th' ? 'th' : 'en',
    author: {
      '@type': 'Organization',
      name: article.author || 'BlessMe Team',
      description: article.authorBio || 'Specialty food sourcing team with 15+ years industry experience',
    },
    publisher: {
      '@type': 'Organization',
      name: 'BlessMe (Thailand) Co., Ltd.',
      url: 'https://blessmethailand.com',
      logo: {
        '@type': 'ImageObject',
        url: 'https://blessmethailand.com/assets/logo-full.png',
      },
    },
    datePublished: article.publishedDate || article.isoDate,
    dateModified: article.updatedDate || article.isoDate,
    url: `https://blessmethailand.com/blog/${article.id}`,
  };

  if (article.img) schema.image = article.img;

  return schema;
}

export function getBlogCategories(lang) {
  const seen = new Set();
  const categories = [];

  ARTICLES.forEach((article) => {
    if (seen.has(article.cat)) return;
    seen.add(article.cat);
    categories.push({
      id: article.cat,
      label: lang === 'th' ? article.catTh || article.cat : article.cat,
    });
  });

  return categories;
}

export function getRelatedArticles(articleId, limit = 3) {
  return ARTICLES.filter((article) => article.id !== articleId).slice(0, limit);
}

export function getAdjacentArticles(articleId) {
  const index = ARTICLES.findIndex((article) => article.id === articleId);
  if (index === -1) return { previous: null, next: null };

  return {
    previous: ARTICLES[index + 1] || null,
    next: ARTICLES[index - 1] || null,
  };
}
